void sol()
{
    vector<char> isPrimes = segmentedSieve(l, r);
    ll ptr = 0;
    for (auto ch : isPrimes)
    {
        if (ch == true)
        {
            cout << l + ptr << en;
        }
        ptr++;
    }
}
